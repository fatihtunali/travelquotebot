import { NextResponse } from 'next/server';
import { v2 as cloudinary } from 'cloudinary';

// Configure Cloudinary
cloudinary.config({
  cloud_name: process.env.CLOUDINARY_CLOUD_NAME,
  api_key: process.env.CLOUDINARY_API_KEY,
  api_secret: process.env.CLOUDINARY_API_SECRET,
});

export async function GET() {
  try {
    console.log('üß™ Testing Cloudinary connection...');
    console.log('Cloud Name:', process.env.CLOUDINARY_CLOUD_NAME);
    console.log('API Key:', process.env.CLOUDINARY_API_KEY ? 'Set ‚úì' : 'Missing ‚úó');
    console.log('API Secret:', process.env.CLOUDINARY_API_SECRET ? 'Set ‚úì' : 'Missing ‚úó');

    // Test the connection by getting account info
    const result = await cloudinary.api.ping();

    console.log('‚úÖ Cloudinary ping successful:', result);

    return NextResponse.json({
      success: true,
      message: '‚úÖ Cloudinary connection successful!',
      config: {
        cloudName: process.env.CLOUDINARY_CLOUD_NAME,
        apiKeySet: !!process.env.CLOUDINARY_API_KEY,
        apiSecretSet: !!process.env.CLOUDINARY_API_SECRET,
      },
      pingResult: result,
    });
  } catch (error: any) {
    console.error('‚ùå Cloudinary test failed:', error);
    return NextResponse.json(
      {
        error: 'Cloudinary connection failed',
        message: error.message,
        details: error.toString(),
      },
      { status: 500 }
    );
  }
}
